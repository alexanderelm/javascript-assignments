/**
 * This function checks if the parameter zeroCheck is less or equal to 0
 * @param zeroCheck - The value that's checked weather it's less, equal or more than 0
 * @returns True if zeroCheck is less or equal 0 and false if its a valuer higher than 0
 */
export const lessOrEqual = (zeroCheck: number): boolean => {
  return zeroCheck <= 0
}
