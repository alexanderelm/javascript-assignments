/**
 * Calculates the area of a triangle by taking base width of the triangle times the height and dividing it by 2
 * @param base - Width of the base of the triangle
 * @param height - Height of the triangle
 * @returns Area of the triangle defined by base and height
 */
export const areaOfTriangle = (base: number, height: number): number => {
  return (base * height) / 2
}
