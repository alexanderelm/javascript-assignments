/**
 * Function checks the num1 and num2 values and returns true if they're equal
 * @param num1 - First value
 * @param num2 - Second value
 * @returns True if the num1 parameter is equal to the num2 parameter
 */
export const equalNumbers = (num1: number, num2: number): boolean => {
  return num1 === num2
}
