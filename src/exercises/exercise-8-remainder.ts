/**
 * Function that finds the remainder of a division with two number (firstNumber and secondNumber) with the % operator
 * @param firstNumber - Number to be divided by secondNumber<<
 * @param secondNumber - Number that that divides firstNumber
 * @returns The remainder og the division
 */
export const remainder = (firstNumber: number, secondNumber: number):number => {
  return firstNumber % secondNumber
}